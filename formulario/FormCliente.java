    public void preencherTabela() {
        ClienteDao cDao = new ClienteDao();
        List<Cliente> lista = cDao.listarTodos();
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setRowCount(0);
        for (Cliente c : lista) {
            modelo.addRow(new Object[]{c.getIdCliente(), c.getNomeCompleto(), c.getCpf(), c.getTelefone(), c.getRg(),
                c.getPessoaReferencia(), c.getNumeroPessoaReferencia()});
        }
    }

    public void preencherTabela(String nome) {
        ClienteDao cDao = new ClienteDao();
        List<Cliente> lista = cDao.listarTodos(nome);
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setRowCount(0);
        for (Cliente c : lista) {
            modelo.addRow(new Object[]{c.getIdCliente(), c.getNomeCompleto(), c.getCpf(), c.getTelefone(), c.getRg(),
                c.getPessoaReferencia(), c.getNumeroPessoaReferencia()});
        }
    }
    
