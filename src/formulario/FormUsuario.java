    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        Usuario u = new Usuario();
        FuncionarioDao fdao = new FuncionarioDao();
        List<Funcionario> lista = fdao.listarTodos(cbFuncionario.getSelectedItem().toString());
        for (Funcionario f : lista) {
            u.setIdFuncionari0(f.getIdFuncionario());
        }
        u.setCpf(edCPF.getText());
        u.setEmail(edEmail.getText());
        u.setNome(edNome.getText());
        u.setSenha(edSenha.getText());
        u.setSobrenome(edSobrenome.getText());
        u.setTelefone(edTelefone.getText());
        u.setUserName(edUsuario.getText());
        UsuarioDao udao = new UsuarioDao();
        udao.inserir(u);
        cbFuncionario.setSelectedIndex(-1);
        edCPF.setText(null);
        edEmail.setText(null);
        edNome.setText(null);
        edSenha.setText(null);
        edSobrenome.setText(null);
        edTelefone.setText(null);
        edUsuario.setText(null);
        preencherTabela();
        tbUsuario.setSelectedIndex(0);
    }//GEN-LAST:event_btCadastrarActionPerformed

