    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        Usuario u = new Usuario();
        int indice = tabela.getSelectedRow();
        preencherTabela(u);
        if (indice >= 0) {
            try {
                u.setIdUsuario(Integer.parseInt(tabela.getValueAt(indice, 0).toString()));
                edNome.setText(tabela.getValueAt(indice, 1).toString());
                edEmail.setText(tabela.getValueAt(indice, 2).toString());
                edUsuario.setText(tabela.getValueAt(indice, 3).toString());
                edTelefone.setText(tabela.getValueAt(indice, 4).toString());
                edCPF.setText(u.getCpf());
                edSobrenome.setText(u.getSobrenome());
                edSenha.setEditable(false);
                btAtualizar.setVisible(true);
                btCadastrar.setVisible(false);
                tbUsuario.setSelectedIndex(1);
            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Erro Ao Carregar Dados");
        }
    }//GEN-LAST:event_btEditarActionPerformed

    private void btIExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIExcluirActionPerformed
        int indice = tabela.getSelectedRow();
        Usuario u = new Usuario();
        UsuarioDao udao = new UsuarioDao();
        if (indice >= 0) {
            u.setIdUsuario(Integer.parseInt(tabela.getValueAt(indice, 0).toString()));
            u.setUserName(tabela.getValueAt(indice, 3).toString());
            udao.deletar(u);
            preencherTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btIExcluirActionPerformed

